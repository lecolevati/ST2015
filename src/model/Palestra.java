package model;

public class Palestra {

	private String ra;
	private String aluno;
	private String titulo; 
	private String dt;
	private String hr; 
	private String titulacao; 
	private String palestrante;
	private String tipo;
	private String curso; 
	private String duracao;
	private String tituloCondensado;
	private int codTitulo;
	
	public String getRa() {
		return ra;
	}
	public void setRa(String ra) {
		this.ra = ra;
	}
	public String getAluno() {
		return aluno;
	}
	public void setAluno(String aluno) {
		this.aluno = aluno;
	}
	public String getTitulo() {
		return titulo;
	}
	public void setTitulo(String titulo) {
		this.titulo = titulo;
	}
	public String getDt() {
		return dt;
	}
	public void setDt(String dt) {
		this.dt = dt;
	}
	public String getHr() {
		return hr;
	}
	public void setHr(String hr) {
		this.hr = hr;
	}
	public String getTitulacao() {
		return titulacao;
	}
	public void setTitulacao(String titulacao) {
		this.titulacao = titulacao;
	}
	public String getPalestrante() {
		return palestrante;
	}
	public void setPalestrante(String palestrante) {
		this.palestrante = palestrante;
	}
	public String getTipo() {
		return tipo;
	}
	public void setTipo(String tipo) {
		this.tipo = tipo;
	}
	public String getCurso() {
		return curso;
	}
	public void setCurso(String curso) {
		this.curso = curso;
	}
	public String getDuracao() {
		return duracao;
	}
	public void setDuracao(String duracao) {
		this.duracao = duracao;
	}
	public String getTituloCondensado() {
		return tituloCondensado;
	}
	public void setTituloCondensado(String tituloCondensado) {
		this.tituloCondensado = tituloCondensado;
	}
	public int getCodTitulo() {
		return codTitulo;
	}
	public void setCodTitulo(int codTitulo) {
		this.codTitulo = codTitulo;
	}

}
